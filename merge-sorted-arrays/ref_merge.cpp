int Merge_Std(const int *aArr, int aCnt, const int *bArr, int bCnt, int *dst) {
  std::merge(aArr, aArr + aCnt, bArr, bArr + bCnt, dst);
  return aCnt + bCnt;
}

int Merge_ScalarTrivial(const int *aArr, int aCnt, const int *bArr, int bCnt, int *dst) {
    int i = 0, j = 0, k = 0;
    while (i < aCnt && j < bCnt) {
        if (aArr[i] < bArr[j])
            dst[k++] = aArr[i++];
        else
            dst[k++] = bArr[j++];
    }

    memcpy(dst + k, aArr + i, (aCnt - i) * sizeof(dst[0]));
    k += (aCnt - i);
    memcpy(dst + k, bArr + j, (bCnt - j) * sizeof(dst[0]));
    k += (bCnt - j);
    return k;
}

int Merge_ScalarOptimized_2(const int *aArr, int aCnt, const int *bArr, int bCnt, int *dst) {
    int i = 0, j = 0, k = 0;
    while (i < aCnt - 32 && j < bCnt - 32) {
        for (int t = 0; t < 32; t++) {
            if (aArr[i] < bArr[j])
                dst[k++] = aArr[i++];
            else
                dst[k++] = bArr[j++];
        }
    }
    while (i < aCnt && j < bCnt) {
        if (aArr[i] < bArr[j])
            dst[k++] = aArr[i++];
        else
            dst[k++] = bArr[j++];
    }

    memcpy(dst + k, aArr + i, (aCnt - i) * sizeof(dst[0]));
    k += (aCnt - i);
    memcpy(dst + k, bArr + j, (bCnt - j) * sizeof(dst[0]));
    k += (bCnt - j);
    return k;
}


int Merge_ScalarOptimized_3(const int *aArr, int aCnt, const int *bArr, int bCnt, int *dst) {
    int lastA = (aCnt > 0 ? aArr[aCnt-1] : INT_MIN);
    int lastB = (bCnt > 0 ? bArr[bCnt-1] : INT_MIN);
    if (lastA < lastB)
        return Merge_ScalarOptimized_3(bArr, bCnt, aArr, aCnt, dst);
    int aCap = aCnt;
    while (aCap > 0 && aArr[aCap-1] >= lastB)
        aCap--;

    int i = 0, j = 0, k = 0;
    int aX = aArr[0], bX = bArr[0];
    for (; k < aCap + bCnt; k++) {
        if (aX < bX) {
            dst[k] = aX;
            aX = aArr[++i];
        }
        else {
            dst[k] = bX;
            bX = bArr[++j];
        }
    }
    assert(i == aCnt || j == bCnt);

    memcpy(dst + k, aArr + i, (aCnt - i) * sizeof(dst[0]));
    k += (aCnt - i);
    memcpy(dst + k, bArr + j, (bCnt - j) * sizeof(dst[0]));
    k += (bCnt - j);
    return k;
}

int Merge_ScalarBranchless_3(const int *aArr, int aCnt, const int *bArr, int bCnt, int *dst) {
    int lastA = (aCnt > 0 ? aArr[aCnt-1] : INT_MIN);
    int lastB = (bCnt > 0 ? bArr[bCnt-1] : INT_MIN);
    if (lastA < lastB)
        return Merge_ScalarBranchless_3(bArr, bCnt, aArr, aCnt, dst);
    int aCap = aCnt;
    while (aCap > 0 && aArr[aCap-1] >= lastB)
        aCap--;

    size_t i = 0, j = 0, k = 0;
    for (; k < aCap + bCnt; k++) {
        int aX = aArr[i], bX = bArr[j];
        dst[k] = (aX < bX ? aX : bX);
        i += (aX < bX);
        j += 1 - (aX < bX);
    }
    //printf("i=%d, aCnt=%d, j=%d, bCnt=%d\n", i, aCnt, j, bCnt);
    assert(i == aCnt || j == bCnt);

    memcpy(dst + k, aArr + i, (aCnt - i) * sizeof(dst[0]));
    k += (aCnt - i);
    memcpy(dst + k, bArr + j, (bCnt - j) * sizeof(dst[0]));
    k += (bCnt - j);
    return k;
}

int Merge_ScalarBranchless_2(const int *aArr, int aCnt, const int *bArr, int bCnt, int *dst) {
    size_t i = 0, j = 0, k = 0;
    while (i < aCnt - 32 && j < bCnt - 32) {
       for (int t = 0; t < 32; t++) {
            int aX = aArr[i], bX = bArr[j];
            dst[k++] = (aX < bX ? aX : bX);
            i += (aX < bX);
            j += 1 - (aX < bX);
        }
    }
    while (i < aCnt && j < bCnt) {
        if (aArr[i] < bArr[j])
            dst[k++] = aArr[i++];
        else
            dst[k++] = bArr[j++];
    }

    memcpy(dst + k, aArr + i, (aCnt - i) * sizeof(dst[0]));
    k += (aCnt - i);
    memcpy(dst + k, bArr + j, (bCnt - j) * sizeof(dst[0]));
    k += (bCnt - j);
    return k;
}

