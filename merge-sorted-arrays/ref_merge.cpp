void TestMergeFunction(int (*mergeFunction)(const int*, int, const int*, int, int*), const char *name, bool checkCorrectness = false) {
  static const int ELEMENTS = 100000000;	//how long to test
    int tries = ELEMENTS / MAXN;

    printf("Testing %s:\n", name);

    int start = clock();
    for (int q = 0; q < tries; q++) {
      if (checkCorrectness) {
          GenMergeInputs();
          Merge_Std(a, an, b, bn, check_res);
      }

        resn = mergeFunction(a, an, b, bn, res);

      if (checkCorrectness) {
        assert(resn == an + bn);
        for (int i = 0; i < resn; i++)
          assert(res[i] == check_res[i]);
      }
    }
    int end = clock();

    double elapsed = double(end - start) / CLOCKS_PER_SEC;
    if (elapsed <= 0.1)
      printf("  WARNING: testing took too little time (%0.3lf ms), timings may be imprecise!\n", elapsed * 1e+3);
    if (checkCorrectness)
      printf("  WARNING: correctness checking mode, timings are incorrect!\n");
    printf("  Time per element: %0.2lf ns\n", 1e+9 * elapsed / (tries * (an + bn)));
    printf("\n");

    assert(std::is_sorted(res, res+resn));
}
